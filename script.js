function loadReviews(language) {
  const container = document.getElementById("reviewOptions");
  container.innerHTML = "";

  reviewData[language].forEach(review => {
    const div = document.createElement("div");
    div.className = "review-option";
    div.textContent = review;
    div.onclick = () => selectReview(review);
    container.appendChild(div);
  });
}

function selectReview(text) {
  document.getElementById("reviewBox").value = text;
  document.getElementById("googleBtn").href = googleReviewLink;
}

function copyReview() {
  const textarea = document.getElementById("reviewBox");
  textarea.select();
  textarea.setSelectionRange(0, 99999);
  document.execCommand("copy");
  alert("Review copied successfully! Now submit it on Google.");
}
